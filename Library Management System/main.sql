CREATE TABLE BOOK (
   BOOKID      INT,
   TITLE       VARCHAR2(30),
   AUTHOR      VARCHAR2(20),
   BRANCH    VARCHAR2(10),
   ISAVAILABLE BOOLEAN,
   CONSTRAINT PK_BOOK_ID PRIMARY KEY (BOOKID)
);

CREATE TABLE USER (
   USERID      NUMBER(38),
   USERNAME    VARCHAR2(30),
   ADDRESS     VARCHAR2(30),
   EMAIL       VARCHAR2(30),
   PHONE       VARCHAR2(30),
   CONSTRAINT PK_USER_ID PRIMARY KEY (USERID)
);

CREATE TABLE LOAN (
   LOANID      NUMBER GENERATED BY DEFAULT AS IDENTITY,
   USERID      NUMBER(38),
   BOOKID      VARCHAR2(10),
   BORROWDATE  DATE,
   RETURNDATE  DATE,
   CONSTRAINT PK_LOAN_ID PRIMARY KEY (LOANID),
   CONSTRAINT FK_USER_ID FOREIGN KEY (USERID) REFERENCES USER (USERID),
   CONSTRAINT FK_BOOK_ID FOREIGN KEY (BOOKID) REFERENCES BOOK (BOOKID)
);
